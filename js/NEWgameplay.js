function randomizeCards(deck) {
    let randDeck = [];
    while (deck.length > 0) {
        let = rand = Math.floor(Math.random() * (deck.length));
        randDeck.push(deck[rand]);
        deck.splice(rand, 1);
    }
    return randDeck;
}

